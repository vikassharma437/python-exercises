#Data Types

#String data type
name = "Vikas Sharma"
print(name)

num1 = "123"
num2 = "456"
print(num1 + num2)
print(num1[0] + " " + num1[1] + " " + num1[2])

#Integer data type
number1 = 123
number2 = 456
print(number1 + number2)

#Float data type
numb3 = 123456789.123
numb4 = round(numb3, 1)
print(numb4)
print("{:.2f}".format(numb3))
