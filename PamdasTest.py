# PEMDAS - Iteration is left to right.
# P - Parenthesis ()
# E - Exponential **
# M - Multiplication *
# D - Division /
# A - Addition +
# S - Substraction -
# Multiplication and Division are equally important.
# Addition and Substraction are also equally important.

print (10 * 6 / 3 + 4 ** 2 - 1)
print (10 * (6 / 3) + 4 ** 2 - 1)